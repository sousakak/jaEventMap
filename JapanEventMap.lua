local p = {}
local getArgs = require( 'Module:Arguments' ).getArgs

function p.main(frame)
    local args = getArgs(frame)
    local wikitext = mw.html.create():wikitext(frame:extensionTag{
        name = 'mapframe',
        content = '\n' .. mw.ext.data.get("jaEventMap.map") .. '\n',
        args = {
            text      = args.text or "日本のオフラインイベントの地図",
            latitude  = "35",
            longitude = "136",
            zoom      = "4",
            width     = args.width or "400",
            height    = args.height or "400",
            lang      = args.lang or "ja"
        }
    })
end

return p